    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed

        
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-mm-yyyy");
        String date = dFormat.format(jDateChooser1.getDate());
        int donationnumber = Integer.parseInt(txt_donationNo.getText());
        String place = txt_place.getText();
        int noofbloodunits = Integer.parseInt(txt_noBloodUnits.getText());
        String did=txt_donorId.getText();
        String dname=txt_fname.getText();
        String btype=txt_blood_group.getText();
        
        
        try
        {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("update blood_detail set units = units+'"+noofbloodunits+"' where blood_group ='"+btype+"'");
            st.executeUpdate("insert into blood_record values('"+donationnumber+"','"+did+"','"+dname+"','"+place+"','"+noofbloodunits+"','"+btype+"','"+date+"')");
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new AddDonation().setVisible(true);
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btn_saveActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        try 
        {
        Connection con = ConnectionProvider.getCon();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT MAX(rec_number) AS max_rec_number FROM blood_record");
        if (rs.next()) 
        {
            int id = rs.getInt("max_rec_number");
            id = id + 1;
            String str = String.valueOf(id);
            txt_donationNo.setText(str);
        } 
        else 
        {
            txt_donationNo.setText("1");
        }
        } 
        catch (SQLException e) 
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_formComponentShown

