    private void txt_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_searchActionPerformed
       String donor_id = txt_donorId.getText();
        try
        {
            Connection con = Project.ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from donor_detail where donor_id = '"+donor_id+"'");
            if(rs.next())
            {
                txt_fname.setText(rs.getString(2));
                txt_blood_group.setText(rs.getString(10));
                txt_donorId.setEditable(false);
            }
            
            else
            {
                JOptionPane.showMessageDialog(null, "Donor ID does not Exist");
            }
        }
        catch(HeadlessException | SQLException e)
        {
            
        }
    }//GEN-LAST:event_txt_searchActionPerformed

